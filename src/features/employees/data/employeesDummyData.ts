export const employeesSnapshot: EmployeesSnapshot = {
  employees: employeeSummaries,
  projectsByEmployeeId: employeeSummaries.reduce<Record<string, ReturnType<typeof defaultProjectsForEmployee>>>(
    (acc, employee) => {
      acc[employee.id] = defaultProjectsForEmployee(employee.id);
      return acc;
    },
    {},
  ),
  vacationsByEmployeeId: employeeSummaries.reduce<Record<string, EmployeesSnapshot['vacationsByEmployeeId'][string]>>(
    (acc, employee, index) => {
      acc[employee.id] = [
        {
          id: `${employee.id}-vacation-1`,
          requestType: 'Vacation',
          periodLabel: 'Sep 13, 2020 - Sep 16, 2020',
          durationLabel: '3d',
          status: index % 3 === 0 ? 'pending' : 'approved',
        },
      ];
      return acc;
    },
    {},
  ),
};
